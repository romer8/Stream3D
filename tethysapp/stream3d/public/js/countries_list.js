// Helper function for the countries //
function makeObject(ini, objectRequest){
  let keysObject = Object.keys(objectRequest);
  let newArrayKeys = [];
  keysObject.forEach(function(e){
    if(e.includes(ini)){
      newArrayKeys.push(e);
    }
  })
  let finalObject = {};
  newArrayKeys.forEach(function(e){
    finalObject[e] = objectRequest[e];
  })
  return finalObject;

}
